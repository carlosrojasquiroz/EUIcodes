function [rg_aux,w_aux]=prices(p,K,L,Z)
%-------------------------------------------------------------------------------------------------------------------------- 
% This function computes the optimal interest rate and wages
%-------------------------------------------------------------------------------------------------------------------------- 
rg_aux=p.alpha.*Z.*(K./L).^(p.alpha-1)-p.delta;
w_aux=(1-p.alpha)*(Z.*(K./L).^p.alpha);
%--------------------------------------------------------------------------------------------------------------------------